    private void listarProdutos (){
        try {
            for(Produto produto : BuscaProduto.Listar()){
                jcbProdutos.addItem(produto.getNome());
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrameRegistrarPedido.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
