    private void createTable(List<Produto> lista) {

        String col[] = { "ID", "Nome", "Valor"};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0);
        for ( Produto item : lista){
            Object[] i = {item.getId(), item.getNome(), formatarMoeda((float) item.getValor()) };  
            tableModel.addRow(i);
        };
        tableProdutos.setModel(tableModel);
        tableProdutos.setDefaultEditor(Object.class, null);
        
    }
    
        public String formatarMoeda (float numero){
        Locale brasil = new Locale("pt", "BR");
        NumberFormat formatadorMoeda = NumberFormat.getCurrencyInstance(brasil);
        return formatadorMoeda.format(numero);
    }

